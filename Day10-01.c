#include<stdio.h>
int main() {
    int i,n,a[100],j;
    printf("Enter the number of elements: ");   
    scanf("%d",&n);
    printf("Enter the elements: ");
    for(i=0;i<n;i++) {
        scanf("%d",&a[i]);
    }
    if 
       (a[i]== a[n-i-1]) {
            printf("Palindrome");
        }
        else {
            printf("Not a palindrome");
        }
        return 0;

}